package com.svvc.hackernews.model;

public class UserProfileForm
{

    private String about;
    private String uemail;
    private String showd;
    private String nopro;
    private String maxv;
    private String mina;
    private String delay;

    public String getDelay()
    {
        return delay;
    }

    public void setDelay(String delay)
    {
        this.delay = delay;
    }

    public String getMina()
    {
        return mina;
    }

    public void setMina(String mina)
    {
        this.mina = mina;
    }

    public String getMaxv()
    {
        return maxv;
    }

    public void setMaxv(String maxv)
    {
        this.maxv = maxv;
    }

    public String getNopro()
    {
        return nopro;
    }

    public void setNopro(String nopro)
    {
        this.nopro = nopro;
    }

    public String getShowd()
    {
        return showd;
    }

    public void setShowd(String showd)
    {
        this.showd = showd;
    }

    public String getUemail()
    {
        return uemail;
    }

    public void setUemail(String uemail)
    {
        this.uemail = uemail;
    }

    public String getAbout()
    {
        return about;
    }

    public void setAbout(String about)
    {
        this.about = about;
    }
}
